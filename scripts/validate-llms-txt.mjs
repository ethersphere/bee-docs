// Validate static/llms.txt against the actual docs directory.
// Reports stale links (URLs pointing to non-existent pages) and
// missing coverage (doc pages not listed in llms.txt).
// Exits 0 — warnings only, does not block the build.

import { readFileSync, readdirSync } from 'node:fs';
import { join, dirname, basename, extname, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const DOCS_DIR = join(ROOT, 'docs');
const LLMS_TXT = join(ROOT, 'static', 'llms.txt');
const BASE_URL = 'https://docs.ethswarm.org/docs/';

/** Recursively find all .md/.mdx files under `dir`. */
function findDocFiles(dir) {
  const results = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      results.push(...findDocFiles(full));
    } else if (/\.(md|mdx)$/.test(entry.name)) {
      results.push(full);
    }
  }
  return results;
}

/** Read the frontmatter `id` from a doc file (returns null if absent). */
function getFrontmatterId(filePath) {
  const content = readFileSync(filePath, 'utf-8');
  const fm = content.match(/^---\n([\s\S]*?)\n---/);
  if (!fm) return null;
  const id = fm[1].match(/^id:\s*(.+)$/m);
  return id ? id[1].trim() : null;
}

/**
 * Compute the URL path segment for a doc file.
 * e.g. docs/bee/installation/shell-script.md  with id "shell-script-install"
 *   → "bee/installation/shell-script-install"
 *
 * When a file's basename matches its parent directory (case-insensitive),
 * Docusaurus treats it as a category index and serves it at the directory
 * URL (with trailing slash).  e.g. DISC/DISC.mdx → "concepts/DISC/"
 */
function getUrlPath(filePath) {
  const relDir = relative(DOCS_DIR, dirname(filePath));
  const id = getFrontmatterId(filePath) || basename(filePath, extname(filePath));
  const dirName = basename(dirname(filePath));
  const fileName = basename(filePath, extname(filePath));

  // Category index: file name matches parent directory (case-insensitive)
  if (dirName.toLowerCase() === fileName.toLowerCase()) {
    return `${relDir}/`;
  }

  return relDir ? `${relDir}/${id}` : id;
}

/** Extract all docs.ethswarm.org/docs/... paths from llms.txt. */
function extractDocPaths(file) {
  const content = readFileSync(file, 'utf-8');
  const escaped = BASE_URL.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const re = new RegExp(`${escaped}([^)\\s]+)`, 'g');
  const paths = [];
  let m;
  while ((m = re.exec(content)) !== null) {
    paths.push(m[1]);
  }
  return paths;
}

// ── Main ────────────────────────────────────────────────────────────

const docFiles = findDocFiles(DOCS_DIR);
const docPathSet = new Set(docFiles.map(getUrlPath));

const llmsPaths = extractDocPaths(LLMS_TXT);
const llmsPathSet = new Set(llmsPaths);

let warnings = 0;

// 1. Stale links — URL in llms.txt but no matching doc file
for (const p of llmsPaths) {
  if (!docPathSet.has(p)) {
    console.warn(`  ⚠  Stale link in llms.txt: ${BASE_URL}${p}`);
    warnings++;
  }
}

// 2. Missing coverage — doc file exists but not in llms.txt
for (const p of docPathSet) {
  if (!llmsPathSet.has(p)) {
    console.warn(`  ⚠  Missing from llms.txt: ${BASE_URL}${p}`);
    warnings++;
  }
}

if (warnings > 0) {
  console.log(`llms.txt validation: ${warnings} warning(s)`);
} else {
  console.log('llms.txt validation: all links verified, full coverage ✓');
}

// 3. Verify slice file references in llms.txt point to files that will be generated
const sliceRe = /https:\/\/docs\.ethswarm\.org\/(llms-[a-z-]+\.txt)/g;
const llmsTxtContent = readFileSync(LLMS_TXT, 'utf-8');
let sliceMatch;
while ((sliceMatch = sliceRe.exec(llmsTxtContent)) !== null) {
  console.log(`  ℹ  Slice file referenced: ${sliceMatch[1]} (generated by docusaurus-plugin-llms at build time)`);
}

// 4. Verify customLLMFiles includePatterns resolve to real files.
//    Parses docusaurus.config.mjs as text to extract patterns, then checks
//    each against the filesystem. Catches typos like "act.md" when the real
//    file is "access-control.md".
// Minimal dependency-free glob (fs.globSync is Node 22+; this repo targets
// Node 20). Matches an includePattern against the file tree under its
// non-wildcard prefix and returns paths relative to `cwd`.
function globMatch(pattern, cwd) {
  const firstStar = pattern.indexOf('*');
  const prefix = pattern.slice(0, firstStar);
  const baseRel = prefix.includes('/') ? prefix.slice(0, prefix.lastIndexOf('/')) : '';
  const re = new RegExp(
    '^' +
      pattern
        .replace(/[.+^${}()|[\]\\]/g, '\\$&')
        .replace(/\*\*/g, '\u0000')
        .replace(/\*/g, '[^/]*')
        .replace(/\u0000/g, '.*') +
      '$'
  );
  const walk = (dir) => {
    let entries;
    try {
      entries = readdirSync(dir, { withFileTypes: true });
    } catch {
      return [];
    }
    const out = [];
    for (const entry of entries) {
      const full = join(dir, entry.name);
      if (entry.isDirectory()) out.push(...walk(full));
      else out.push(relative(cwd, full));
    }
    return out;
  };
  return walk(join(cwd, baseRel)).filter((f) => re.test(f));
}

const configText = readFileSync(join(ROOT, 'docusaurus.config.mjs'), 'utf-8');
const patternRe = /includePatterns:\s*\[([\s\S]*?)\]/g;
let patternBlock;
let patternWarnings = 0;

while ((patternBlock = patternRe.exec(configText)) !== null) {
  const patterns = patternBlock[1].match(/'([^']+)'/g)?.map(s => s.replace(/'/g, '')) ?? [];
  for (const pattern of patterns) {
    // For glob patterns (contain *), match against the tree; for exact paths, check existence
    if (pattern.includes('*')) {
      const matches = globMatch(pattern, ROOT);
      if (matches.length === 0) {
        console.warn(`  ⚠  includePattern matches no files: ${pattern}`);
        patternWarnings++;
      }
    } else {
      try {
        readFileSync(join(ROOT, pattern));
      } catch {
        console.warn(`  ⚠  includePattern file not found: ${pattern}`);
        patternWarnings++;
      }
    }
  }
}

if (patternWarnings > 0) {
  console.log(`includePatterns validation: ${patternWarnings} pattern(s) match no files`);
} else {
  console.log('includePatterns validation: all patterns resolve ✓');
}
